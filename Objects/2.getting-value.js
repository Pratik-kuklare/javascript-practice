const person3 = {
     firstName : "Pratik",
    lastName : "Kuklare",
    age : 20,
    skills : [
        'HTML',
        'CSS',
        'JavaScript'
    ],
    isStudent : true
}
// . dot notation method
console.log(person3.firstName);
console.log(person3.lastName);
console.log(person3.age);
console.log(person3.skills);

// [] bracket notation method
console.log(person3['firstName']);
console.log(person3['lastName']);
console.log(person3['age']);
console.log(person3['skills']);